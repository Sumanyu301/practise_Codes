#include <bits/stdc++.h>
using namespace std;

int main()
{
    int t;
    cin >> t;
    while (t--)
    {
        int n;
        cin >> n;
        long long sum = 0;
        int arr[n];
        for (int i = 0; i < n; i++)
        {
            cin >> arr[i];
        }
        int index = 0;
        while(arr[index]==0)
        {
            index++;
        }
        for (int i = index; i < n; i++)
        {
            if (arr[i] == 0)
            {
                for (int j = index; j < i; j++)
                {
                    if (arr[j] > 0)
                    {
                        sum++;
                        arr[j]--;
                        arr[i]++;
                        break;
                    }
                }
            }
        }
        for (int i = 0; i < n - 1; i++)
        {
            sum += arr[i];
        }
        cout << sum << endl;
    }
    return 0;
}
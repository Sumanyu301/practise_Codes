#include <bits/stdc++.h>
using namespace std;

int main()
{
    int t;
    cin >> t;
    while (t--)
    {
        int total, hs, ms, hh, mm;
        cin >> total >> hh >> mm;
        int hi, mi;
        int arr[total];
        int x = 0;
        while (total--)
        {
            cin >> hi;
            cin >> mi;
            hs = hh;
            ms = mm;
            int sum = 0;
            while (true)
            {
                if (hi == hs)
                {
                    if (mi == ms)
                    {
                        arr[x] = sum;
                        x++;
                        break;
                    }
                }
                ms++;
                sum++;
                if (ms >= 60)
                {
                    hs++;
                    ms = 0;
                }
                if (hs == 24)
                {
                    hs = 0;
                }
            }
        }
        int smallest = *min_element(arr, arr + x);
        cout << smallest / 60 << " " << smallest % 60 << endl;
    }
    return 0;
}
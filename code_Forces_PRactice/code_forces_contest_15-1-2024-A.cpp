#include <bits/stdc++.h>
using namespace std;
int main()
{
    int t;
    cin >> t;
    while (t--)
    {
        long long x1, y1, x2, y2, x3, y3, x4, y4;
        cin >> x1 >> y1 >> x2 >> y2 >> x3 >> y3 >> x4 >> y4;
        long long dx;
        long long dy;
        if(x1==x2)
        {
            dx = x2-x1;
            dy = y2-y1;
        }
        else if(x1==x3)
        {
            dx = x3-x1;
            dy = y3-y1;
        }
        else if(x1==x4)
        {
            dx = x4-x1;
            dy = y4-y1;
        }
        long long len =  sqrt(dx * dx + dy * dy);
        cout << len*len << endl;
    }

    return 0;
}
#include <bits/stdc++.h>
using namespace std;

int main()
{
    int t;
    cin >> t;
    while (t--)
    {
        long long n;
        long long f;
        long long a;
        long long b;
        cin >> n >> f >> a >> b;
        long long arr[n];
        long long res[n];
        for (long long i = 0; i < n; i++)
        {
            cin >> arr[i];
            res[i] = 0;
        }
        for (long long i = 0; i < n; i++)
        {
            if (i == 0)
            {
                res[i] = arr[i];
            }
            else
            {
                res[i] = arr[i] - arr[i - 1];
            }
        }
        for (long long i = 0; i < n; i++)
        {
            if (res[i] * a < b)
            {
                f -= (res[i] * a);
            }
            else
            {
                f -= b;
            }
        }
        if (f > 0)
        {
            cout << "YES" << endl;
        }
        else
        {
            cout << "NO" << endl;
        }
    }
    return 0;
}
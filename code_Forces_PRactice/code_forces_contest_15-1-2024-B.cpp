#include <bits/stdc++.h>
using namespace std;

int main()
{
    int t;
    cin >> t;
    while (t--)
    {
        int n;
        cin >> n;
        char arr[n];
        char brr[n];
        int res[n];
        for (int i = 0; i < n; i++)
        {
            cin >> arr[i];
            res[i] = 0;
        }
        for (int i = 0; i < n; i++)
        {
            cin >> brr[i];
        }
        for (int i = 0; i < n; i++)
        {
            if (arr[i] == brr[i])
            {
                ;
            }
            else if (arr[i] == '1' && brr[i]=='0')
            {
                res[i] = 10;
            }
            else
            {
                res[i] = 9;
            }
        }
        int count1 = 0;
        int count2 = 0;
        for (int i = 0; i < n; i++)
        {
            if (res[i] == 10)
            {
                count1++;
            }
            else if (res[i] == 9)
            {
                count2++;
            }
        }
        int maxCount = max(count1, count2);
        int count = (maxCount);
        cout << count << endl;
    }

    return 0;
}